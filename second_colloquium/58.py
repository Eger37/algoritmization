# 58. Дан одновимірний масив цілих чисел. Знайдіть, скільки разів в ньому
# повторюється найчастіше число.
import numpy as np

a = np.random.randint(0, 10, 100)
print(f"Дан одновимірний масив цілих чисел: {a}")
# num = a[0]
c = 1
while len(a) != 0:
    if len(a[a == a[0]]) > c:
        c = len(a[a == a[0]])
        # num = a[0]
    a = a[a != a[0]]
    print(a)
print(f"Стільки разів в ньому повторюється найчастіше число: {c}")
