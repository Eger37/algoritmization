# 44. Підрахуйте кількість елементів одновимірного масиву, які збігаються зі
# своїм номером і при цьому кратні 3.
import numpy as np

size = 100
print(f"size: {size}")
a = np.random.randint(0, 100, size)
print(f"Вхідний масив: {a}")
c = 0
for i in range(len(a)):
    if a[i] == i and a[i] % 3 == 0:
        c += 1
print(f"кількість елементів одновимірного масиву, які збігаються зі своїм номером і при цьому кратні 3: {c}")
