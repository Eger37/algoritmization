# 40. Обчислити суму парних елементів одновимірного масиву до першого
# зустрінутого нульового елемента.
import numpy as np

a = np.array(
    [15, 19, -10, 0, -13, 18, -13, 14, 15, -18])

if 0 in a:
    a = a[:(np.where(a == 0))[0][0]:]
    a = a[::2]
    print(f"парні елементи масиву до першого входження нуля: {a}")
    print(f"кількість парних елементів одновимірного масиву до першого заданого числа: {a.sum()}")
else:
    print(f"Нульовий елемент відсутній")
