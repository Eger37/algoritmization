# 52. Знайти найбільший елемент з елементів одновимірного масиву, що мають
# парний номер. Визначити, чи є він єдиним.
import numpy as np

a = np.random.randint(0, 101, 10)
a = a[a % 2 == 0]
m = max(a)
idx = (np.where(a == m))[0][0]
a[idx] = min(a)
if max(a) == m:
    print("найбільший елемент є єдиним у масиві")
else:
    print("найбільший елемент не є єдиним у масиві")
